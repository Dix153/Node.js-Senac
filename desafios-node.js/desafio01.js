// Valor em graus Celsius
let temperaturaCelsius = 25; // Exemplo: 25 graus Celsius

// Conversão para Fahrenheit
let temperaturaFahrenheit = (temperaturaCelsius * 9/5) + 32; //formula usada para conversão.

// Exibindo o resultado no console
console.log(`${temperaturaCelsius}°C é igual a ${temperaturaFahrenheit}°F`);